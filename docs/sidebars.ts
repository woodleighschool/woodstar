import type { SidebarsConfig } from "@docusaurus/plugin-content-docs";

import rawApiSidebar from "./content/api/sidebar";

// The OpenAPI plugin emits one flat file per operation plus this sidebar slice,
// grouped by tag, whose default export is the items array. Drop its generated
// info page; the hand-written overview is the API landing instead.
type ApiSidebar = Extract<NonNullable<SidebarsConfig["apiSidebar"]>, unknown[]>;
type ApiItem = ApiSidebar[number];
const apiSidebarItems = (
  Array.isArray(rawApiSidebar) ? rawApiSidebar : (rawApiSidebar as { default: ApiItem[] }).default
) as ApiItem[];
const apiGroups = apiSidebarItems.filter(
  (item) =>
    !(
      typeof item === "object" &&
      "type" in item &&
      item.type === "doc" &&
      "id" in item &&
      item.id === "api/woodstar-api"
    ),
);

const sidebars: SidebarsConfig = {
  // Guide: autogenerated from the content/ tree, so each folder's
  // _category_.json becomes a section. The generated API reference is kept out
  // of this sidebar by sidebarItemsGenerator in docusaurus.config.ts, since it
  // has its own navbar menu below.
  docsSidebar: [{ type: "autogenerated", dirName: "." }],
  apiSidebar: [{ type: "doc", id: "api/overview" }, ...apiGroups],
};

export default sidebars;
